void main() {
  // variables in dart
  int firstValue = 23;
  int secoundValue = 32;
  print(firstValue + secoundValue);
  String One = "hello";
  String Two = "World";
  print(One + Two);
  dynamic someValue = 23;
  print(someValue);
  // another way of creating variables
  var otherValue = 10;
  print(otherValue);
  // final run time constant
  final otherValue2 = 10;
  print(otherValue2);
  // const compile time constant
  const otherValue3 = '10';
  print(otherValue3);
  // to assign null value
  int? value1 = null;
  print(value1);
}
